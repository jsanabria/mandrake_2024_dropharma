use function PHPMaker2024\mandrake\EncryptPassword;

/**
 * Entity class for "usuario_master" table
 */
#[Entity]
#[Table(name: "usuario_master")]
class UsuarioMaster extends AbstractEntity
{
    #[Id]
    #[Column(type: "integer", unique: true)]
    #[GeneratedValue]
    private int $id;

    #[Column(type: "string", nullable: true)]
    private ?string $username;

    #[Column(type: "string", nullable: true)]
    private ?string $username2;

    public function getId(): int
    {
        return $this->id;
    }

    public function setId(int $value): static
    {
        $this->id = $value;
        return $this;
    }

    public function getUsername(): ?string
    {
        return HtmlDecode($this->username);
    }

    public function setUsername(?string $value): static
    {
        $this->username = RemoveXss($value);
        return $this;
    }

    public function getUsername2(): ?string
    {
        return HtmlDecode($this->username2);
    }

    public function setUsername2(?string $value): static
    {
        $this->username2 = RemoveXss($value);
        return $this;
    }
}
